##Programa calcula á média aritimética entre dois números!

n1 = float(input('Digite a primeira nota do aluno: '))
n2 = float(input('Digite a segunda nota do aluno: '))
media = (n1 + n2) / 2
print('A primeira nota do aluno foi {}, e a segunda foi {}; Portanto a média alcançada foi {}'.format(n1, n2, media))
 
